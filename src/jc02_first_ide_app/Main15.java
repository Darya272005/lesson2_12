package jc02_first_ide_app;

public class Main15 {

	public static void main(String[] args) {
	
		        double pi = Math.PI;

		        double piSquared = Math.pow(pi, 2);
		        double piCubed = Math.pow(pi, 3);
		        double piToFourth = Math.pow(pi, 4);

		        System.out.println("Первая степень числа π: " + pi);
		        System.out.println("Вторая степень числа π: " + piSquared);
		        System.out.println("Третья степень числа π: " + piCubed);
		        System.out.println("Четвертая степень числа π: " + piToFourth);

	}

}
